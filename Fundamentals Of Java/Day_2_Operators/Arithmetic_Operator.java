package Day_2_Operators;
public class Arithmetic_Operator
{
    public static void main(String[] args)
    {
        double num1 = 3.664;
        int num2 = 4;
        System.out.println(num1 + num2);
        System.out.println(num1 - num2);
        System.out.println(num1 * num2);
        System.out.println(num1 / num2);
        System.out.println(num1 % num2);

    }
}