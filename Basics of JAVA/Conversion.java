public class Conversion
{
    public static void main(String[] args)
    {
        // Conversion which is also called automatic conversion
        byte b=100;
        int i=b;
        System.out.println(i);

        // casting which is converting it yourself also called explicit conversion
        int c =357;
        byte k=(byte) c;
        System.out.println(k);
    }

}

